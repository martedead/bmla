function [result, history] = bmla_v4_1_radius_consistent_solver(fun, x0, options)
%BMLA_V4_1_RADIUS_CONSISTENT_SOLVER Radius-consistent BMLA trust region.
%
% BMLA v4.1 preserves the frozen v3 local-affine architecture and the v4.0
% actual/predicted-reduction globalization. Its defining change is that the
% scale of the affine model is tied to the trust-region radius.
%
% At iteration k, candidate model samples satisfy
%
%   ||x_i - x_k||_2 <= gamma_model * Delta_k,
%
% and the regression uses normalized coordinates
%
%   z_i = (x_i - x_k)/Delta_k.
%
% If the local normalized displacements do not span R^n, geometry-restoring
% samples are evaluated along missing singular-vector directions. Finite
% rejected trials remain in the global sample archive but enter a model only
% when they are radius-local, nonduplicate, and selected by QR-maximin.
%
% The trust-region subproblem remains
%
%   min_h 0.5*||F(x_k) + A_k h||_2^2,  ||h||_2 <= Delta_k,
%
% with the same v4.0 acceptance, contraction, and expansion thresholds.
%
    arguments
        fun (1,1) function_handle
        x0 (:,1) double
        options struct
    end

    validate_options(options);

    xInitial = x0(:);
    numberOfVariables = numel(xInitial);

    initialScale = get_option( ...
        options, 'initialScale', 0.9);

    baseMemoryLength = get_option( ...
        options, 'memoryLength', numberOfVariables + 2);

    maxWindowLength = get_option( ...
        options, 'maxWindowLength', 3*(numberOfVariables + 2));

    maxConditionNumber = get_option( ...
        options, 'maxConditionNumber', 1e8);

    regularizationRelative = get_option( ...
        options, 'regularizationRelative', 1e-8);

    maxRelativeStep = get_option( ...
        options, 'maxRelativeStep', 1.0);

    stagnationPatience = get_option( ...
        options, 'stagnationPatience', 3);

    minimumRelativeResidualImprovement = get_option( ...
        options, ...
        'minimumRelativeResidualImprovement', ...
        1e-3);

    initialTrustRegionRelative = get_option( ...
        options, 'initialTrustRegionRelative', 0.50);

    minimumTrustRegionRelative = get_option( ...
        options, 'minimumTrustRegionRelative', 1e-10);

    maximumTrustRegionRelative = get_option( ...
        options, 'maximumTrustRegionRelative', 1.00);

    acceptanceThreshold = get_option( ...
        options, 'acceptanceThreshold', 0.10);

    shrinkThreshold = get_option( ...
        options, 'shrinkThreshold', 0.25);

    expansionThreshold = get_option( ...
        options, 'expansionThreshold', 0.75);

    radiusShrinkFactor = get_option( ...
        options, 'radiusShrinkFactor', 0.25);

    radiusExpansionFactor = get_option( ...
        options, 'radiusExpansionFactor', 2.00);

    boundaryFraction = get_option( ...
        options, 'boundaryFraction', 0.80);

    maximumConsecutiveRejectedTrials = get_option( ...
        options, ...
        'maximumConsecutiveRejectedTrials', ...
        12);

    minimumPredictedReductionRelative = get_option( ...
        options, ...
        'minimumPredictedReductionRelative', ...
        1e-12);


    localModelRadiusFactor = get_option( ...
        options, 'localModelRadiusFactor', 3.0);

    duplicateDistanceRelative = get_option( ...
        options, 'duplicateDistanceRelative', 1e-8);

    restorationStepFraction = get_option( ...
        options, 'restorationStepFraction', 0.80);

    maximumRestorationSamplesPerIteration = get_option( ...
        options, ...
        'maximumRestorationSamplesPerIteration', ...
        numberOfVariables);

    maximumGeometryRestorationRounds = get_option( ...
        options, ...
        'maximumGeometryRestorationRounds', ...
        2);

    maxFunctionEvaluations = get_option( ...
        options, ...
        'maxFunctionEvaluations', ...
        Inf);

    fInitial = fun(xInitial);
    fInitial = fInitial(:);

    functionEvaluations = 1;

    if any(~isfinite(xInitial)) || any(~isfinite(fInitial))
        error( ...
            'BMLAV4:InvalidInitialPoint', ...
            'The initial point or residual is nonfinite.');
    end

    sampleX = xInitial.';
    sampleF = fInitial.';

    history = initialize_v4_history( ...
        xInitial, fInitial, numberOfVariables);

    %% Coordinate initialization, preserving the v3 sampling pattern.

    initializationBudgetExhausted = false;

    for variableIndex = 1:numberOfVariables

        remainingFunctionEvaluations = ...
            maxFunctionEvaluations - functionEvaluations;

        if remainingFunctionEvaluations <= 0
            initializationBudgetExhausted = true;
            break;
        end

        requestedPerturbation = ...
            (initialScale - 1)*xInitial(variableIndex);

        if abs(requestedPerturbation) <= ...
                eps(max(1, abs(xInitial(variableIndex))))

            requestedPerturbation = ...
                -(1 - initialScale)* ...
                max(1, abs(xInitial(variableIndex)));
        end

        [xSample, fSample, actualPerturbation, ...
            initializationEvaluationCount, ...
            initializationSampleFound] = ...
            finite_initialization_sample( ...
                fun, ...
                xInitial, ...
                variableIndex, ...
                requestedPerturbation, ...
                remainingFunctionEvaluations);

        functionEvaluations = ...
            functionEvaluations + ...
            initializationEvaluationCount;

        if ~initializationSampleFound

            if functionEvaluations >= ...
                    maxFunctionEvaluations

                initializationBudgetExhausted = true;
                break;
            end

            error( ...
                'BMLAV41:InvalidInitializationSample', ...
                ['Unable to construct a finite initialization ' ...
                 'sample for variable %d.'], ...
                variableIndex);
        end

        sampleX(end + 1, :) = xSample.';
        sampleF(end + 1, :) = fSample.';

        previousPoint = history.x(:, end);
        initializationStep = xSample - previousPoint;

        history = append_initialization_history( ...
            history, ...
            xSample, ...
            fSample, ...
            initializationStep, ...
            functionEvaluations);
    end

    xCurrent = sampleX(end, :).';
    fCurrent = sampleF(end, :).';

    initialFunctionEvaluations = functionEvaluations;

    radiusScale = max(1, norm(xCurrent, 2));

    trustRegionRadius = ...
        initialTrustRegionRelative*radiusScale;

    minimumTrustRegionRadius = ...
        minimumTrustRegionRelative*radiusScale;

    maximumTrustRegionRadius = ...
        maximumTrustRegionRelative*radiusScale;

    trustRegionRadius = min( ...
        maximumTrustRegionRadius, ...
        max(minimumTrustRegionRadius, trustRegionRadius));

    converged = false;
    stopReason = "maximum_iterations";
    completedIterations = 0;

    stagnationCounter = 0;
    consecutiveRejectedTrials = 0;

    acceptedTrials = 0;
    rejectedTrials = 0;
    trustRegionShrinks = 0;
    trustRegionExpansions = 0;


    geometryRestorationSamples = 0;
    geometryRestorationEvaluations = 0;
    geometryRestorationIterations = 0;

    if norm(fCurrent, 2) < options.tolF
        converged = true;
        stopReason = "residual_tolerance";
    else

        for iteration = 1:options.maxIterations

            if functionEvaluations >= ...
                    maxFunctionEvaluations

                stopReason = ...
                    "maximum_function_evaluations";
                completedIterations = iteration - 1;
                break;
            end

            radiusScale = max(1, norm(xCurrent, 2));

            minimumTrustRegionRadius = ...
                minimumTrustRegionRelative*radiusScale;

            maximumTrustRegionRadius = ...
                maximumTrustRegionRelative*radiusScale;

            hardStepCap = ...
                maxRelativeStep*radiusScale;

            effectiveRadius = min( ...
                [ ...
                    trustRegionRadius, ...
                    maximumTrustRegionRadius, ...
                    hardStepCap ...
                ]);

            effectiveRadius = max( ...
                minimumTrustRegionRadius, ...
                effectiveRadius);

            [A, b, modelInfo, selectionInfo] = ...
                select_radius_consistent_local_model( ...
                    sampleX, ...
                    sampleF, ...
                    xCurrent, ...
                    effectiveRadius, ...
                    localModelRadiusFactor, ...
                    duplicateDistanceRelative, ...
                    baseMemoryLength, ...
                    maxWindowLength, ...
                    numberOfVariables, ...
                    maxConditionNumber);

            geometryRankBeforeRestoration = ...
                selectionInfo.geometryRank;

            restorationSamplesThisIteration = 0;
            restorationEvaluationsThisIteration = 0;
            restorationRoundsThisIteration = 0;

            while selectionInfo.needsGeometryRestoration && ...
                    restorationRoundsThisIteration < ...
                        maximumGeometryRestorationRounds

                [sampleX, sampleF, restorationInfo] = ...
                    restore_radius_consistent_geometry( ...
                        fun, ...
                        sampleX, ...
                        sampleF, ...
                        xCurrent, ...
                        effectiveRadius, ...
                        localModelRadiusFactor, ...
                        duplicateDistanceRelative, ...
                        maxWindowLength, ...
                        numberOfVariables, ...
                        restorationStepFraction, ...
                        maximumRestorationSamplesPerIteration, ...
                        maxFunctionEvaluations - ...
                            functionEvaluations);

                functionEvaluations = ...
                    functionEvaluations + ...
                    restorationInfo.functionEvaluations;

                restorationSamplesThisIteration = ...
                    restorationSamplesThisIteration + ...
                    restorationInfo.samplesAdded;

                restorationEvaluationsThisIteration = ...
                    restorationEvaluationsThisIteration + ...
                    restorationInfo.functionEvaluations;

                restorationRoundsThisIteration = ...
                    restorationRoundsThisIteration + 1;

                if restorationInfo.samplesAdded == 0
                    break;
                end

                [A, b, modelInfo, selectionInfo] = ...
                    select_radius_consistent_local_model( ...
                        sampleX, ...
                        sampleF, ...
                        xCurrent, ...
                        effectiveRadius, ...
                        localModelRadiusFactor, ...
                        duplicateDistanceRelative, ...
                        baseMemoryLength, ...
                        maxWindowLength, ...
                        numberOfVariables, ...
                        maxConditionNumber);
            end

            if restorationSamplesThisIteration > 0
                geometryRestorationIterations = ...
                    geometryRestorationIterations + 1;
            end

            geometryRestorationSamples = ...
                geometryRestorationSamples + ...
                restorationSamplesThisIteration;

            geometryRestorationEvaluations = ...
                geometryRestorationEvaluations + ...
                restorationEvaluationsThisIteration;

            selectionInfo.geometryRankBeforeRestoration = ...
                geometryRankBeforeRestoration;

            selectionInfo.restorationSamplesAdded = ...
                restorationSamplesThisIteration;

            selectionInfo.restorationFunctionEvaluations = ...
                restorationEvaluationsThisIteration;

            selectionInfo.restorationRounds = ...
                restorationRoundsThisIteration;

            if functionEvaluations >= ...
                    maxFunctionEvaluations

                stepInfo.lambda = NaN;
                stepInfo.usedRegularization = false;
                stepInfo.stepKind = ...
                    "function_evaluation_budget";
                stepInfo.predictedReduction = NaN;

                completedIterations = iteration;

                history = append_v4_iteration_history( ...
                    history, ...
                    xCurrent, ...
                    fCurrent, ...
                    zeros(numberOfVariables, 1), ...
                    functionEvaluations, ...
                    iteration, ...
                    modelInfo, ...
                    selectionInfo, ...
                    stepInfo, ...
                    trustRegionRadius, ...
                    effectiveRadius, ...
                    NaN, ...
                    NaN, ...
                    false, ...
                    stagnationCounter, ...
                    "function_evaluation_budget");

                stopReason = ...
                    "maximum_function_evaluations";
                break;
            end

            if isempty(A) || ...
                    any(~isfinite(A(:))) || ...
                    any(~isfinite(b))

                stopReason = "invalid_linear_model";
                break;
            end

            [step, stepInfo] = ...
                compute_affine_trust_region_step( ...
                    A, ...
                    fCurrent, ...
                    effectiveRadius, ...
                    selectionInfo.modelAccepted, ...
                    regularizationRelative, ...
                    minimumPredictedReductionRelative);

            if any(~isfinite(step)) || ...
                    stepInfo.predictedReduction <= 0

                trustRegionRadius = max( ...
                    minimumTrustRegionRadius, ...
                    radiusShrinkFactor*effectiveRadius);

                trustRegionShrinks = ...
                    trustRegionShrinks + 1;

                rejectedTrials = rejectedTrials + 1;
                consecutiveRejectedTrials = ...
                    consecutiveRejectedTrials + 1;

                completedIterations = iteration;

                history = append_v4_iteration_history( ...
                    history, ...
                    xCurrent, ...
                    fCurrent, ...
                    zeros(numberOfVariables, 1), ...
                    functionEvaluations, ...
                    iteration, ...
                    modelInfo, ...
                    selectionInfo, ...
                    stepInfo, ...
                    trustRegionRadius, ...
                    effectiveRadius, ...
                    -Inf, ...
                    -Inf, ...
                    false, ...
                    stagnationCounter, ...
                    "invalid_model_step");

                if consecutiveRejectedTrials >= ...
                        maximumConsecutiveRejectedTrials

                    stopReason = ...
                        "consecutive_rejected_trials";
                    break;
                end

                if trustRegionRadius <= ...
                        minimumTrustRegionRadius*(1 + 10*eps)

                    stopReason = "trust_region_too_small";
                    break;
                end

                continue;
            end

            if functionEvaluations >= ...
                    maxFunctionEvaluations

                stepInfo.stepKind = ...
                    "function_evaluation_budget";

                completedIterations = iteration;

                history = append_v4_iteration_history( ...
                    history, ...
                    xCurrent, ...
                    fCurrent, ...
                    zeros(numberOfVariables, 1), ...
                    functionEvaluations, ...
                    iteration, ...
                    modelInfo, ...
                    selectionInfo, ...
                    stepInfo, ...
                    trustRegionRadius, ...
                    effectiveRadius, ...
                    NaN, ...
                    NaN, ...
                    false, ...
                    stagnationCounter, ...
                    "function_evaluation_budget");

                stopReason = ...
                    "maximum_function_evaluations";
                break;
            end

            xTrial = xCurrent + step;
            fTrial = fun(xTrial);
            fTrial = fTrial(:);

            functionEvaluations = ...
                functionEvaluations + 1;

            trialFinite = ...
                all(isfinite(xTrial)) && ...
                all(isfinite(fTrial));

            currentMerit = 0.5*(fCurrent.'*fCurrent);

            predictedReduction = ...
                stepInfo.predictedReduction;

            if trialFinite

                trialMerit = 0.5*(fTrial.'*fTrial);
                actualReduction = ...
                    currentMerit - trialMerit;

                acceptanceRatio = ...
                    actualReduction / ...
                    predictedReduction;

            else

                actualReduction = -Inf;
                acceptanceRatio = -Inf;
            end

            acceptedTrial = ...
                trialFinite && ...
                actualReduction > 0 && ...
                acceptanceRatio >= ...
                    acceptanceThreshold;

            % Every finite expensive evaluation is useful model information,
            % even when the trust-region trial is rejected.
            if trialFinite
                sampleX(end + 1, :) = xTrial.';
                sampleF(end + 1, :) = fTrial.';
            end

            radiusBeforeUpdate = trustRegionRadius;

            if ~trialFinite || ...
                    acceptanceRatio < shrinkThreshold

                trustRegionRadius = max( ...
                    minimumTrustRegionRadius, ...
                    radiusShrinkFactor*effectiveRadius);

                trustRegionShrinks = ...
                    trustRegionShrinks + 1;

            elseif acceptedTrial && ...
                    acceptanceRatio > expansionThreshold && ...
                    norm(step, 2) >= ...
                        boundaryFraction*effectiveRadius

                trustRegionRadius = min( ...
                    maximumTrustRegionRadius, ...
                    radiusExpansionFactor* ...
                        trustRegionRadius);

                trustRegionExpansions = ...
                    trustRegionExpansions + 1;
            end

            if acceptedTrial

                [stop, reason, metrics, ...
                    stagnationCounter] = ...
                    evaluate_patient_stopping( ...
                        xCurrent, ...
                        xTrial, ...
                        fCurrent, ...
                        fTrial, ...
                        iteration, ...
                        options.tolX, ...
                        options.tolF, ...
                        options.maxIterations, ...
                        stagnationCounter, ...
                        stagnationPatience, ...
                        minimumRelativeResidualImprovement);

                xCurrent = xTrial;
                fCurrent = fTrial;

                acceptedTrials = acceptedTrials + 1;
                consecutiveRejectedTrials = 0;

                phaseLabel = "accepted_trial";

            else

                stop = false;
                reason = "continue";

                metrics.stepNorm = 0;
                metrics.smallStep = false;
                metrics.relativeResidualImprovement = NaN;
                metrics.productiveSmallStep = false;
                metrics.residualNorm = norm(fCurrent, 2);

                rejectedTrials = rejectedTrials + 1;
                consecutiveRejectedTrials = ...
                    consecutiveRejectedTrials + 1;

                phaseLabel = "rejected_trial";
            end

            history = append_v4_iteration_history( ...
                history, ...
                xCurrent, ...
                fCurrent, ...
                step, ...
                functionEvaluations, ...
                iteration, ...
                modelInfo, ...
                selectionInfo, ...
                stepInfo, ...
                trustRegionRadius, ...
                effectiveRadius, ...
                actualReduction, ...
                acceptanceRatio, ...
                acceptedTrial, ...
                stagnationCounter, ...
                phaseLabel);

            completedIterations = iteration;

            if acceptedTrial && stop
                stopReason = reason;
                converged = ...
                    reason == "residual_tolerance";
                break;
            end

            if ~acceptedTrial && ...
                    consecutiveRejectedTrials >= ...
                        maximumConsecutiveRejectedTrials

                stopReason = ...
                    "consecutive_rejected_trials";
                break;
            end

            if ~acceptedTrial && ...
                    trustRegionRadius <= ...
                        minimumTrustRegionRadius*(1 + 10*eps)

                stopReason = "trust_region_too_small";
                break;
            end

            if iteration >= options.maxIterations
                stopReason = "maximum_iterations";
            end
        end
    end

    finalRadiusScale = max(1, norm(xCurrent, 2));

    finalEffectiveRadius = min( ...
        [ ...
            trustRegionRadius, ...
            maximumTrustRegionRelative*finalRadiusScale, ...
            maxRelativeStep*finalRadiusScale ...
        ]);

    finalEffectiveRadius = max( ...
        minimumTrustRegionRelative*finalRadiusScale, ...
        finalEffectiveRadius);

    [finalA, finalB, finalModelInfo, ...
        finalSelectionInfo] = ...
        select_radius_consistent_local_model( ...
            sampleX, ...
            sampleF, ...
            xCurrent, ...
            finalEffectiveRadius, ...
            localModelRadiusFactor, ...
            duplicateDistanceRelative, ...
            baseMemoryLength, ...
            maxWindowLength, ...
            numberOfVariables, ...
            maxConditionNumber);

    result.x = xCurrent;
    result.f = fCurrent;
    result.normF = norm(fCurrent, 2);

    result.iterations = completedIterations;
    result.functionEvaluations = functionEvaluations;
    result.initialFunctionEvaluations = ...
        initialFunctionEvaluations;

    result.maxFunctionEvaluations = ...
        maxFunctionEvaluations;

    result.budgetExhausted = ...
        isfinite(maxFunctionEvaluations) && ...
        functionEvaluations >= ...
            maxFunctionEvaluations && ...
        stopReason == ...
            "maximum_function_evaluations";

    if isfinite(maxFunctionEvaluations)
        result.remainingFunctionEvaluations = max( ...
            0, ...
            maxFunctionEvaluations - ...
                functionEvaluations);
    else
        result.remainingFunctionEvaluations = Inf;
    end

    result.converged = converged;
    result.stopReason = stopReason;

    result.acceptedTrials = acceptedTrials;
    result.rejectedTrials = rejectedTrials;
    result.trustRegionShrinks = trustRegionShrinks;
    result.trustRegionExpansions = ...
        trustRegionExpansions;


    result.geometryRestorationSamples = ...
        geometryRestorationSamples;

    result.geometryRestorationEvaluations = ...
        geometryRestorationEvaluations;

    result.geometryRestorationIterations = ...
        geometryRestorationIterations;

    result.finalTrustRegionRadius = ...
        trustRegionRadius;

    result.regularizedIterations = ...
        sum(history.usedRegularization);

    result.cauchyIterations = ...
        sum(history.stepKind == "cauchy");

    finiteRatios = history.acceptanceRatio( ...
        isfinite(history.acceptanceRatio));

    if isempty(finiteRatios)
        result.meanAcceptanceRatio = NaN;
        result.medianAcceptanceRatio = NaN;
    else
        result.meanAcceptanceRatio = ...
            mean(finiteRatios);
        result.medianAcceptanceRatio = ...
            median(finiteRatios);
    end

    result.finalA = finalA;
    result.finalB = finalB;
    result.finalMatrixRank = ...
        finalModelInfo.matrixRank;
    result.finalConditionNumber = ...
        finalModelInfo.conditionNumber;
    result.finalModelResidual = ...
        finalModelInfo.residualNorm;
    result.finalPoolSize = ...
        finalSelectionInfo.poolSize;
    result.finalSelectedSamples = ...
        finalSelectionInfo.selectedSamples;


    result.finalModelRadius = ...
        finalSelectionInfo.modelRadius;

    result.finalNormalizationRadius = ...
        finalSelectionInfo.normalizationRadius;

    result.finalGeometryRank = ...
        finalSelectionInfo.geometryRank;

    result.finalLocalCandidateSamples = ...
        finalSelectionInfo.localCandidateSamples;

    result.maximumSelectedNormalizedDistance = ...
        max(history.maximumSelectedNormalizedDistance);

    result.allSelectedSamplesWithinModelRadius = ...
        all(history.allSelectedWithinModelRadius);

    result.allModelsContainedCurrentPoint = ...
        all(history.currentPointIncluded);

    result.maximumCurrentPointDistance = ...
        max(history.currentPointDistance);

    result.initialTrustRegionRelative = ...
        initialTrustRegionRelative;
    result.minimumTrustRegionRelative = ...
        minimumTrustRegionRelative;
    result.maximumTrustRegionRelative = ...
        maximumTrustRegionRelative;
    result.acceptanceThreshold = ...
        acceptanceThreshold;
    result.shrinkThreshold = shrinkThreshold;
    result.expansionThreshold = expansionThreshold;
    result.radiusShrinkFactor = ...
        radiusShrinkFactor;
    result.radiusExpansionFactor = ...
        radiusExpansionFactor;
    result.maximumConsecutiveRejectedTrials = ...
        maximumConsecutiveRejectedTrials;

    result.localModelRadiusFactor = ...
        localModelRadiusFactor;

    result.duplicateDistanceRelative = ...
        duplicateDistanceRelative;

    result.restorationStepFraction = ...
        restorationStepFraction;

    result.maximumRestorationSamplesPerIteration = ...
        maximumRestorationSamplesPerIteration;

    result.maximumGeometryRestorationRounds = ...
        maximumGeometryRestorationRounds;
end


function history = initialize_v4_history( ...
        xInitial, fInitial, numberOfVariables)
%INITIALIZE_V4_HISTORY

    history.x = xInitial;
    history.f = fInitial;
    history.normF = norm(fInitial, 2);

    history.step = zeros(numberOfVariables, 1);
    history.stepNorm = 0;

    history.matrixRank = NaN;
    history.conditionNumber = NaN;
    history.designMatrixRank = NaN;
    history.designConditionNumber = NaN;
    history.modelResidual = NaN;

    history.poolSize = NaN;
    history.selectedSamples = NaN;
    history.selectionExpansion = NaN;
    history.modelAccepted = false;
    history.currentPointIncluded = true;
    history.currentPointDistance = 0;

    history.modelRadius = NaN;
    history.normalizationRadius = NaN;
    history.localCandidateSamples = NaN;
    history.uniqueLocalSamples = NaN;
    history.geometryRank = NaN;
    history.geometryRankBeforeRestoration = NaN;
    history.maximumSelectedNormalizedDistance = 0;
    history.allSelectedWithinModelRadius = true;
    history.restorationSamplesAdded = 0;
    history.restorationFunctionEvaluations = 0;
    history.restorationRounds = 0;

    history.usedRegularization = false;
    history.regularizationLambda = NaN;
    history.stepKind = "initial";

    history.trustRegionRadius = NaN;
    history.effectiveRadius = NaN;
    history.predictedReduction = NaN;
    history.actualReduction = NaN;
    history.acceptanceRatio = NaN;
    history.acceptedTrial = false;

    history.stagnationCounter = 0;
    history.iteration = 0;
    history.functionEvaluations = 1;
    history.phase = "initial";
end


function history = append_initialization_history( ...
        history, xSample, fSample, step, functionEvaluations)
%APPEND_INITIALIZATION_HISTORY

    history.x(:, end + 1) = xSample;
    history.f(:, end + 1) = fSample;
    history.normF(end + 1) = norm(fSample, 2);

    history.step(:, end + 1) = step;
    history.stepNorm(end + 1) = norm(step, 2);

    history.matrixRank(end + 1) = NaN;
    history.conditionNumber(end + 1) = NaN;
    history.designMatrixRank(end + 1) = NaN;
    history.designConditionNumber(end + 1) = NaN;
    history.modelResidual(end + 1) = NaN;

    history.poolSize(end + 1) = NaN;
    history.selectedSamples(end + 1) = NaN;
    history.selectionExpansion(end + 1) = NaN;
    history.modelAccepted(end + 1) = false;
    history.currentPointIncluded(end + 1) = true;
    history.currentPointDistance(end + 1) = 0;

    history.modelRadius(end + 1) = NaN;
    history.normalizationRadius(end + 1) = NaN;
    history.localCandidateSamples(end + 1) = NaN;
    history.uniqueLocalSamples(end + 1) = NaN;
    history.geometryRank(end + 1) = NaN;
    history.geometryRankBeforeRestoration(end + 1) = NaN;
    history.maximumSelectedNormalizedDistance(end + 1) = 0;
    history.allSelectedWithinModelRadius(end + 1) = true;
    history.restorationSamplesAdded(end + 1) = 0;
    history.restorationFunctionEvaluations(end + 1) = 0;
    history.restorationRounds(end + 1) = 0;

    history.usedRegularization(end + 1) = false;
    history.regularizationLambda(end + 1) = NaN;
    history.stepKind(end + 1) = "initialization";

    history.trustRegionRadius(end + 1) = NaN;
    history.effectiveRadius(end + 1) = NaN;
    history.predictedReduction(end + 1) = NaN;
    history.actualReduction(end + 1) = NaN;
    history.acceptanceRatio(end + 1) = NaN;
    history.acceptedTrial(end + 1) = false;

    history.stagnationCounter(end + 1) = 0;
    history.iteration(end + 1) = 0;
    history.functionEvaluations(end + 1) = ...
        functionEvaluations;
    history.phase(end + 1) = "initialization";
end


function history = append_v4_iteration_history( ...
        history, xCurrent, fCurrent, step, ...
        functionEvaluations, iteration, ...
        modelInfo, selectionInfo, stepInfo, ...
        trustRegionRadius, effectiveRadius, ...
        actualReduction, acceptanceRatio, ...
        acceptedTrial, stagnationCounter, phaseLabel)
%APPEND_V4_ITERATION_HISTORY

    history.x(:, end + 1) = xCurrent;
    history.f(:, end + 1) = fCurrent;
    history.normF(end + 1) = norm(fCurrent, 2);

    history.step(:, end + 1) = step;
    history.stepNorm(end + 1) = norm(step, 2);

    history.matrixRank(end + 1) = ...
        modelInfo.matrixRank;
    history.conditionNumber(end + 1) = ...
        modelInfo.conditionNumber;
    history.designMatrixRank(end + 1) = ...
        modelInfo.designMatrixRank;
    history.designConditionNumber(end + 1) = ...
        modelInfo.designConditionNumber;
    history.modelResidual(end + 1) = ...
        modelInfo.residualNorm;

    history.poolSize(end + 1) = ...
        selectionInfo.poolSize;
    history.selectedSamples(end + 1) = ...
        selectionInfo.selectedSamples;
