function [poolIndices, currentGlobalIndex, localCandidateSamples] = ...
        radius_local_unique_indices( ...
            sampleX, xCurrent, modelRadius, ...
            normalizationRadius, duplicateDistanceRelative, ...
            maxWindowLength)
%RADIUS_LOCAL_UNIQUE_INDICES Recent, radius-local, nonduplicate samples.

    numberOfSamples = size(sampleX, 1);

    poolStartIndex = max( ...
        1, ...
        numberOfSamples - ...
        maxWindowLength + 1);

    recentIndices = ...
        (poolStartIndex:numberOfSamples).';

    allDistances = vecnorm( ...
        sampleX - xCurrent.', ...
        2, ...
        2);

    minimumDistance = min(allDistances);

    currentTolerance = ...
        100*eps(max(1, norm(xCurrent, 2)));

    currentCandidates = find( ...
        allDistances <= ...
        minimumDistance + currentTolerance);

    currentGlobalIndex = ...
        currentCandidates(end);

    radiusTolerance = ...
        100*eps(max(1, modelRadius));

    localRecentIndices = recentIndices( ...
        allDistances(recentIndices) <= ...
        modelRadius + radiusTolerance);

    localCandidateSamples = ...
        numel(localRecentIndices);

    candidateOrder = [ ...
        currentGlobalIndex; ...
        flipud(setdiff( ...
            localRecentIndices, ...
            currentGlobalIndex, ...
            'stable')) ...
    ];

    keptIndices = currentGlobalIndex;
    keptNormalizedCoordinates = zeros(1, size(sampleX, 2));

    % Use an explicit scalar index instead of transposing candidateOrder.
    % For a singleton candidateOrder, candidateOrder(2:end).' can become a
    % 0-by-1 matrix. MATLAB then executes one loop iteration with an empty
    % loop variable, which caused the assignment failure found by the first
    % v4.1 smoke test.
    candidateOrder = candidateOrder(:);

    for orderPosition = 2:numel(candidateOrder)

        candidateIndex = ...
            candidateOrder(orderPosition);

        candidateNormalized = ...
            (sampleX(candidateIndex, :) - ...
             xCurrent.')/normalizationRadius;

        distancesToKept = vecnorm( ...
            keptNormalizedCoordinates - ...
            candidateNormalized, ...
            2, ...
            2);

        if all(distancesToKept > ...
                duplicateDistanceRelative)

            keptIndices(end + 1, 1) = ...
                candidateIndex;

            keptNormalizedCoordinates(end + 1, :) = ...
                candidateNormalized;
        end
    end

    poolIndices = keptIndices;
end


function selectedIndices = ...
        select_radius_informative_indices( ...
            poolZ, referenceIndex, ...
            selectedSamples, numberOfVariables)
%SELECT_RADIUS_INFORMATIVE_INDICES QR-maximin in Delta-normalized space.

    poolSize = size(poolZ, 1);

    selectedSamples = min( ...
        selectedSamples, ...
        poolSize);

    if selectedSamples == 1
        selectedIndices = referenceIndex;
        return;
    end

    candidateIndices = setdiff( ...
        (1:poolSize).', ...
        referenceIndex, ...
        'stable');

    normalizedDifferences = ...
        poolZ(candidateIndices, :) - ...
        poolZ(referenceIndex, :);

    numberRequiredBeforeReference = ...
        selectedSamples - 1;

    selectedCandidatePositions = ...
        zeros(0, 1);

    if ~isempty(normalizedDifferences)

        try
            [~, ~, pivotOrder] = ...
                qr( ...
                    normalizedDifferences.', ...
                    'vector');
        catch
            [~, ~, permutationMatrix] = ...
                qr(normalizedDifferences.');
            [~, pivotOrder] = ...
                max(permutationMatrix, [], 1);
        end

        numberOfQRPicks = min( ...
            [ ...
                numberOfVariables, ...
                numberRequiredBeforeReference, ...
                numel(pivotOrder) ...
            ]);

        selectedCandidatePositions = ...
            pivotOrder(1:numberOfQRPicks).';
    end

    allCandidatePositions = ...
        (1:numel(candidateIndices)).';

    while numel(selectedCandidatePositions) < ...
            numberRequiredBeforeReference

        availablePositions = setdiff( ...
            allCandidatePositions, ...
            selectedCandidatePositions, ...
            'stable');

        if isempty(availablePositions)
            break;
        end

        selectedCoordinates = ...
            poolZ(referenceIndex, :);

        if ~isempty(selectedCandidatePositions)
            selectedCoordinates = [ ...
                selectedCoordinates
                poolZ( ...
                    candidateIndices( ...
                        selectedCandidatePositions), :) ...
            ];
        end

        bestPosition = ...
            availablePositions(1);
        bestMinimumDistance = -Inf;

        for candidatePosition = ...
                availablePositions.'

            candidateCoordinates = ...
                poolZ( ...
                    candidateIndices(candidatePosition), :);

            distances = vecnorm( ...
                selectedCoordinates - ...
                candidateCoordinates, ...
                2, ...
                2);

            candidateMinimumDistance = ...
                min(distances);

            if candidateMinimumDistance > ...
                    bestMinimumDistance

                bestMinimumDistance = ...
                    candidateMinimumDistance;
                bestPosition = ...
                    candidatePosition;
            end
        end

        selectedCandidatePositions(end + 1, 1) = ...
            bestPosition;
    end

    selectedIndices = [ ...
        candidateIndices( ...
            selectedCandidatePositions)
        referenceIndex ...
    ];

    selectedIndices = unique( ...
        selectedIndices, ...
        'stable');

    if numel(selectedIndices) < selectedSamples

        remainingIndices = setdiff( ...
            (1:poolSize).', ...
            selectedIndices, ...
            'stable');

        numberMissing = ...
            selectedSamples - ...
            numel(selectedIndices);

        selectedIndices = [ ...
            selectedIndices
            remainingIndices(1:numberMissing) ...
        ];
    end

    if ~ismember(referenceIndex, selectedIndices)
        error( ...
            'BMLAV41:CurrentPointNotSelected', ...
            'The accepted iterate was not retained in the model.');
    end
end


function [rankValue, rightSingularVectors] = ...
        normalized_geometry_rank( ...
            displacementRows, numberOfVariables)
%NORMALIZED_GEOMETRY_RANK Rank of Delta-normalized displacement rows.
%
% The singular-value extraction must remain scalar-safe for every matrix
% shape. In MATLAB, when S is 1-by-n or n-by-1, DIAG(S) interprets S as a
% vector and constructs a diagonal matrix instead of extracting the
% diagonal. That made rankValue a row vector for a one-direction geometry
% pool and subsequently broke scalar logical operators.

    if isempty(displacementRows)
        rankValue = 0;
        rightSingularVectors = eye(numberOfVariables);
        return;
    end

    [~, singularMatrix, rightSingularVectors] = ...
        svd(displacementRows, 'econ');

    diagonalLength = min(size(singularMatrix));

    if diagonalLength == 0
        singularValues = zeros(0, 1);
    else
        squareLeadingBlock = singularMatrix( ...
            1:diagonalLength, ...
            1:diagonalLength);

        singularValues = diag(squareLeadingBlock);
        singularValues = singularValues(:);
    end

    if isempty(singularValues)
        rankValue = 0;
    else
        tolerance = ...
            max(size(displacementRows))* ...
            eps(max(singularValues));

        rankValue = nnz( ...
            singularValues > tolerance);
    end

    % Enforce the public contract explicitly: geometry rank is one scalar
    % integer in [0, numberOfVariables].
    rankValue = double(rankValue(1));
    rankValue = max( ...
        0, ...
        min(numberOfVariables, rankValue));

    if size(rightSingularVectors, 2) < numberOfVariables
        rightSingularVectors = [ ...
            rightSingularVectors, ...
            null(rightSingularVectors.') ...
        ];
    end
end


function [A, b, info] = ...
        fit_radius_normalized_affine_model( ...
            sampleX, sampleF, xReference, ...
            normalizationRadius, numberOfVariables)
%FIT_RADIUS_NORMALIZED_AFFINE_MODEL SVD regression in z=(x-xk)/Delta.

    numberOfSamples = size(sampleX, 1);

    normalizedX = ...
        (sampleX - xReference.')/ ...
        normalizationRadius;

    designMatrix = [ ...
        ones(numberOfSamples, 1), ...
        normalizedX];

    [U, S, V] = svd(designMatrix, 'econ');
    singularValues = diag(S);

    if isempty(singularValues)
        designTolerance = 0;
        designRank = 0;
        coefficients = zeros( ...
            numberOfVariables + 1, ...
            size(sampleF, 2));
    else
        designTolerance = ...
            max(size(designMatrix))* ...
            eps(max(singularValues));

        designRank = sum( ...
            singularValues > designTolerance);

        if designRank == 0
            coefficients = zeros( ...
                numberOfVariables + 1, ...
                size(sampleF, 2));
        else
            projectedData = ...
                U(:, 1:designRank).'*sampleF;

            scaledData = bsxfun( ...
                @rdivide, ...
                projectedData, ...
                singularValues(1:designRank));

            coefficients = ...
                V(:, 1:designRank)*scaledData;
        end
    end

    info.designMatrixRank = designRank;
    info.designRankTolerance = designTolerance;

    if designRank >= numberOfVariables + 1
        info.designConditionNumber = ...
            singularValues(1)/ ...
            singularValues(numberOfVariables + 1);
    else
        info.designConditionNumber = Inf;
    end

    c = coefficients(1, :).';
    normalizedSlope = ...
        coefficients(2:end, :).';

    A = normalizedSlope/normalizationRadius;
    b = c - A*xReference;

    predictedF = designMatrix*coefficients;

    info.residualNorm = norm( ...
        predictedF - sampleF, ...
        'fro');

    info.normalizationRadius = ...
        normalizationRadius;

    if any(~isfinite(A(:))) || any(~isfinite(b))
        info.matrixRank = 0;
        info.matrixRankTolerance = NaN;
        info.conditionNumber = Inf;
        return;
    end

    singularValuesA = svd(A);

    if isempty(singularValuesA)
        matrixTolerance = 0;
        matrixRank = 0;
    else
        matrixTolerance = ...
            max(size(A))*eps(max(singularValuesA));
        matrixRank = sum( ...
            singularValuesA > matrixTolerance);
    end

    info.matrixRank = matrixRank;
    info.matrixRankTolerance = matrixTolerance;

    if matrixRank >= numberOfVariables
        info.conditionNumber = ...
            singularValuesA(1)/ ...
            singularValuesA(numberOfVariables);
    else
        info.conditionNumber = Inf;
    end
end


function info = ...
        empty_radius_model_info( ...
            numberOfVariables, normalizationRadius)
%EMPTY_RADIUS_MODEL_INFO

    info.designMatrixRank = 0;
    info.designRankTolerance = NaN;
    info.designConditionNumber = Inf;
    info.residualNorm = Inf;
    info.matrixRank = 0;
    info.matrixRankTolerance = NaN;
    info.conditionNumber = Inf;
    info.normalizationRadius = normalizationRadius;
end


function value = ...
        get_option(options, fieldName, defaultValue)
%GET_OPTION

    fieldName = char(fieldName);

    if isfield(options, fieldName)

        value = options.(fieldName);

    else

        value = defaultValue;
    end
end


function validate_options(options)
%VALIDATE_OPTIONS

    requiredFields = { ...
        'tolX', ...
        'tolF', ...
        'maxIterations'};

    for fieldIndex = 1:numel(requiredFields)

        fieldName = requiredFields{fieldIndex};

        if ~isfield(options, fieldName)

            error( ...
                'BMLAV41:MissingOption', ...
                'Falta options.%s.', ...
                fieldName);
        end
    end

    if ~isscalar(options.tolX) || ...
            options.tolX <= 0 || ...
            ~isfinite(options.tolX)

        error( ...
            'BMLAV41:InvalidTolX', ...
            'tolX debe ser un escalar positivo.');
    end

    if ~isscalar(options.tolF) || ...
            options.tolF <= 0 || ...
            ~isfinite(options.tolF)

        error( ...
            'BMLAV41:InvalidTolF', ...
            'tolF debe ser un escalar positivo.');
    end

    if ~isscalar(options.maxIterations) || ...
            options.maxIterations < 1 || ...
            fix(options.maxIterations) ~= ...
            options.maxIterations

        error( ...
            'BMLAV41:' + ...
            "InvalidMaxIterations", ...
            'maxIterations debe ser un entero positivo.');
    end

    if isfield(options, 'maxFunctionEvaluations')

        validInfiniteBudget = ...
            isinf(options.maxFunctionEvaluations) && ...
            options.maxFunctionEvaluations > 0;

        validFiniteBudget = ...
            isfinite(options.maxFunctionEvaluations) && ...
            options.maxFunctionEvaluations >= 1 && ...
            fix(options.maxFunctionEvaluations) == ...
                options.maxFunctionEvaluations;

        if ~isscalar(options.maxFunctionEvaluations) || ...
                ~(validInfiniteBudget || validFiniteBudget)

            error( ...
                'BMLAV41:InvalidMaxFunctionEvaluations', ...
                ['maxFunctionEvaluations must be a positive ' ...
                 'integer or Inf.']);
        end
    end

    if isfield(options, 'initialScale')

        if ~isscalar(options.initialScale) || ...
                ~isfinite(options.initialScale) || ...
                options.initialScale == 1

            error( ...
                'BMLAV41:' + ...
                "InvalidInitialScale", ...
                'initialScale debe ser finito y diferente de 1.');
        end
    end

    if isfield(options, 'memoryLength')

        if ~isscalar(options.memoryLength) || ...
                options.memoryLength < 1 || ...
                fix(options.memoryLength) ~= ...
                options.memoryLength

            error( ...
                'BMLAV41:' + ...
                "InvalidMemoryLength", ...
                'memoryLength debe ser un entero positivo.');
        end
    end

    if isfield(options, 'maxWindowLength')

        if ~isscalar(options.maxWindowLength) || ...
                options.maxWindowLength < 1 || ...
                fix(options.maxWindowLength) ~= ...
                options.maxWindowLength

            error( ...
                'BMLAV41:' + ...
                "InvalidMaxWindowLength", ...
                'maxWindowLength debe ser un entero positivo.');
        end
    end

    if isfield(options, 'maxConditionNumber')

        if ~isscalar(options.maxConditionNumber) || ...
                options.maxConditionNumber <= 1

            error( ...
                'BMLAV41:' + ...
                "InvalidConditionLimit", ...
                'maxConditionNumber debe ser mayor que 1.');
        end
    end

    if isfield(options, 'regularizationRelative')

        if ~isscalar(options.regularizationRelative) || ...
                options.regularizationRelative <= 0 || ...
                ~isfinite(options.regularizationRelative)

            error( ...
                'BMLAV41:' + ...
                "InvalidRegularization", ...
                'regularizationRelative debe ser positivo.');
        end
    end

    if isfield(options, 'maxRelativeStep')

        if ~isscalar(options.maxRelativeStep) || ...
                options.maxRelativeStep <= 0

            error( ...
                'BMLAV41:' + ...
                "InvalidMaximumStep", ...
                'maxRelativeStep debe ser positivo.');
        end
    end

    if isfield(options, 'stagnationPatience')

        if ~isscalar(options.stagnationPatience) || ...
                options.stagnationPatience < 1 || ...
                fix(options.stagnationPatience) ~= ...
                options.stagnationPatience

            error( ...
                'BMLAV41:' + ...
                "InvalidStagnationPatience", ...
                'stagnationPatience debe ser un entero positivo.');
        end
    end

    if isfield( ...
            options, ...
            'minimumRelativeResidualImprovement')

        value = ...
            options.minimumRelativeResidualImprovement;

        if ~isscalar(value) || ...
                value < 0 || ...
                ~isfinite(value)

            error( ...
                'BMLAV41:' + ...
                "InvalidResidualImprovement", ...
                ['minimumRelativeResidualImprovement ' ...
                 'debe ser no negativo.']);
        end
    end
end
