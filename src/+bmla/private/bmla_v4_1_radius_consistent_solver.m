function [xSample, fSample, actualPerturbation, ...
        evaluationCount, success] = ...
        finite_initialization_sample( ...
            fun, xInitial, variableIndex, ...
            requestedPerturbation, ...
            maximumEvaluations)
%FINITE_INITIALIZATION_SAMPLE Back off only when initialization overflows.
%
% Every attempted residual evaluation is counted. The routine never exceeds
% maximumEvaluations and reports success explicitly.

    actualPerturbation = requestedPerturbation;
    evaluationCount = 0;
    success = false;

    xSample = xInitial;
    fSample = NaN(size(xInitial));

    maximumAttempts = min( ...
        24, ...
        floor(maximumEvaluations));

    for attempt = 1:maximumAttempts

        xSample = xInitial;
        xSample(variableIndex) = ...
            xInitial(variableIndex) + ...
            actualPerturbation;

        fSample = fun(xSample);
        fSample = fSample(:);

        evaluationCount = ...
            evaluationCount + 1;

        if all(isfinite(xSample)) && ...
                all(isfinite(fSample))

            success = true;
            return;
        end

        actualPerturbation = ...
            0.5*actualPerturbation;
    end
end


function [step, info] = ...
        compute_affine_trust_region_step( ...
            A, fCurrent, trustRadius, ...
            modelAccepted, regularizationRelative, ...
            minimumPredictedReductionRelative)
%COMPUTE_AFFINE_TRUST_REGION_STEP
%
% Solve the linear least-squares trust-region subproblem through the SVD:
%
%   min 0.5*||f + A h||^2,  ||h|| <= Delta.
%
% A Levenberg-Marquardt multiplier is found by bracketing and bisection.
% If numerical cancellation makes the predicted reduction nonpositive, a
% Cauchy step for the same model is used.

    numberOfVariables = size(A, 2);

    [U, S, V] = svd(A, 'econ');
    singularValues = diag(S);

    if isempty(singularValues)
        step = zeros(numberOfVariables, 1);
        info.lambda = NaN;
        info.usedRegularization = true;
        info.stepKind = "invalid";
        info.predictedReduction = -Inf;
        return;
    end

    matrixTolerance = ...
        max(size(A))*eps(max(singularValues));

    fullRank = ...
        sum(singularValues > matrixTolerance) >= ...
        numberOfVariables;

    if modelAccepted && fullRank
        lambdaFloor = 0;
    else
        lambdaFloor = ...
            regularizationRelative* ...
            max(singularValues(1)^2, 1);
    end

    [stepAtFloor, floorNorm] = ...
        lm_step_from_svd( ...
            U, singularValues, V, ...
            fCurrent, lambdaFloor, ...
            matrixTolerance);

    lambda = lambdaFloor;

    if floorNorm > trustRadius

        lambdaLow = lambdaFloor;
        lambdaHigh = max( ...
            [ ...
                1e-16, ...
                regularizationRelative* ...
                    max(singularValues(1)^2, 1), ...
                lambdaFloor ...
            ]);

        [stepHigh, highNorm] = ...
            lm_step_from_svd( ...
                U, singularValues, V, ...
                fCurrent, lambdaHigh, ...
                matrixTolerance);

        expansionCounter = 0;

        while highNorm > trustRadius && ...
                expansionCounter < 80

            lambdaHigh = max( ...
                2*lambdaHigh, ...
                lambdaHigh + 1e-16);

            [stepHigh, highNorm] = ...
                lm_step_from_svd( ...
                    U, singularValues, V, ...
                    fCurrent, lambdaHigh, ...
                    matrixTolerance);

            expansionCounter = ...
                expansionCounter + 1;
        end

        if highNorm > trustRadius

            step = ...
                trustRadius* ...
                stepHigh/max(highNorm, eps);

            lambda = lambdaHigh;

        else

            for bisectionIndex = 1:60

                lambdaMid = ...
                    0.5*(lambdaLow + lambdaHigh);

                [stepMid, midNorm] = ...
                    lm_step_from_svd( ...
                        U, singularValues, V, ...
                        fCurrent, lambdaMid, ...
                        matrixTolerance);

                if midNorm > trustRadius
                    lambdaLow = lambdaMid;
                else
                    lambdaHigh = lambdaMid;
                    stepHigh = stepMid;
                end
            end

            step = stepHigh;
            lambda = lambdaHigh;
        end

    else

        step = stepAtFloor;
    end

    modelResidual = fCurrent + A*step;

    currentMerit = 0.5*(fCurrent.'*fCurrent);
    modelMerit = 0.5*(modelResidual.'*modelResidual);

    predictedReduction = ...
        currentMerit - modelMerit;

    minimumPredictedReduction = ...
        minimumPredictedReductionRelative* ...
        max(currentMerit, eps);

    stepKind = "newton";

    if lambda > 0
        stepKind = "levenberg_marquardt";
    end

    if ~isfinite(predictedReduction) || ...
            predictedReduction <= ...
                minimumPredictedReduction

        gradient = A.'*fCurrent;
        gradientNorm = norm(gradient, 2);

        if gradientNorm == 0 || ...
                ~isfinite(gradientNorm)

            step = zeros(numberOfVariables, 1);
            predictedReduction = -Inf;
            stepKind = "invalid";

        else

            AGradient = A*gradient;
            denominator = AGradient.'*AGradient;
            numerator = gradient.'*gradient;

            if denominator > 0 && isfinite(denominator)
                alpha = numerator/denominator;
            else
                alpha = trustRadius/gradientNorm;
            end

            alpha = min( ...
                alpha, ...
                trustRadius/gradientNorm);

            step = -alpha*gradient;

            modelResidual = fCurrent + A*step;

            predictedReduction = ...
                currentMerit - ...
                0.5*(modelResidual.'*modelResidual);

            stepKind = "cauchy";
            lambda = NaN;
        end
    end

    info.lambda = lambda;
    info.usedRegularization = ...
        stepKind == "levenberg_marquardt";
    info.stepKind = stepKind;
    info.predictedReduction = ...
        predictedReduction;
end


function [step, stepNorm] = ...
        lm_step_from_svd( ...
            U, singularValues, V, ...
            fCurrent, lambda, matrixTolerance)
%LM_STEP_FROM_SVD Stable LM step for a fixed multiplier.

    projectedResidual = U.'*fCurrent;

    weights = zeros(size(singularValues));

    active = ...
        singularValues > matrixTolerance;

    if lambda > 0
        weights = ...
            singularValues ./ ...
            (singularValues.^2 + lambda);
    else
        weights(active) = ...
            1 ./ singularValues(active);
    end

    step = -V*(weights.*projectedResidual);
    stepNorm = norm(step, 2);
end




function [stop, reason, metrics, newCounter] = ...
        evaluate_patient_stopping( ...
            xCurrent, xNext, fCurrent, fNext, iteration, ...
            tolX, tolF, maxIterations, ...
            currentCounter, stagnationPatience, ...
            minimumRelativeResidualImprovement)
%EVALUATE_PATIENT_STOPPING
% Aplica convergencia residual estricta y estancamiento paciente.

    metrics.stepNorm = norm( ...
        xNext - xCurrent, ...
        2);

    metrics.xNorm = norm(xCurrent, 2);

    metrics.residualNorm = norm(fNext, 2);

    metrics.previousResidualNorm = ...
        norm(fCurrent, 2);

    metrics.stepThreshold = ...
        tolX*(metrics.xNorm + tolX);

    metrics.smallStep = ...
        metrics.stepNorm <= ...
        metrics.stepThreshold;

    metrics.relativeResidualImprovement = ...
        (metrics.previousResidualNorm - ...
         metrics.residualNorm) / ...
        max(metrics.previousResidualNorm, eps);

    metrics.productiveSmallStep = ...
        metrics.smallStep && ...
        metrics.relativeResidualImprovement >= ...
        minimumRelativeResidualImprovement;

    numericalFailure = ...
        any(~isfinite(xNext)) || ...
        any(~isfinite(fNext));

    residualSatisfied = ...
        metrics.residualNorm < tolF;

    stop = false;
    reason = "continue";

    if numericalFailure

        newCounter = currentCounter;
        stop = true;
        reason = "numerical_failure";
        return;
    end

    if residualSatisfied

        newCounter = 0;
        stop = true;
        reason = "residual_tolerance";
        return;
    end

    if metrics.smallStep

        if metrics.productiveSmallStep

            newCounter = 0;

        else

            newCounter = ...
                currentCounter + 1;
        end

    else

        newCounter = 0;
    end

    if newCounter >= stagnationPatience

        stop = true;
        reason = "stagnation";
        return;
    end

    if iteration >= maxIterations

        stop = true;
        reason = "maximum_iterations";
    end
end


function [sampleX, sampleF, info] = ...
        restore_radius_consistent_geometry( ...
            fun, sampleX, sampleF, xCurrent, ...
            effectiveRadius, localModelRadiusFactor, ...
            duplicateDistanceRelative, maxWindowLength, ...
            numberOfVariables, restorationStepFraction, ...
            maximumRestorationSamples, ...
            maximumFunctionEvaluations)
%RESTORE_RADIUS_CONSISTENT_GEOMETRY Add samples in missing local directions.

    normalizationRadius = max( ...
        effectiveRadius, ...
        100*eps(max(1, norm(xCurrent, 2))));

    modelRadius = ...
        localModelRadiusFactor*normalizationRadius;

    [localIndices, currentGlobalIndex] = ...
        radius_local_unique_indices( ...
            sampleX, ...
            xCurrent, ...
            modelRadius, ...
            normalizationRadius, ...
            duplicateDistanceRelative, ...
            maxWindowLength);

    localX = sampleX(localIndices, :);
    localZ = ...
        (localX - xCurrent.')/normalizationRadius;

    currentLocalIndex = find( ...
        localIndices == currentGlobalIndex, ...
        1, ...
        'last');

    displacementRows = localZ;
    displacementRows(currentLocalIndex, :) = [];

    [geometryRank, rightSingularVectors] = ...
        normalized_geometry_rank( ...
            displacementRows, ...
            numberOfVariables);

    missingDirections = ...
        rightSingularVectors(:, ...
            geometryRank + 1:numberOfVariables);

    numberRequested = min( ...
        [ ...
            size(missingDirections, 2), ...
            maximumRestorationSamples ...
        ]);

    samplesAdded = 0;
    functionEvaluations = 0;
    failedDirections = 0;

    baseStepLength = ...
        restorationStepFraction*normalizationRadius;

    for directionIndex = 1:numberRequested

        remainingFunctionEvaluations = ...
            maximumFunctionEvaluations - ...
            functionEvaluations;

        if remainingFunctionEvaluations <= 0
            break;
        end

        direction = ...
            missingDirections(:, directionIndex);

        [xSample, fSample, evaluationCount, success] = ...
            finite_restoration_sample( ...
                fun, ...
                xCurrent, ...
                direction, ...
                baseStepLength, ...
                sampleX, ...
                normalizationRadius, ...
                duplicateDistanceRelative, ...
                remainingFunctionEvaluations);

        functionEvaluations = ...
            functionEvaluations + evaluationCount;

        if success
            sampleX(end + 1, :) = xSample.';
            sampleF(end + 1, :) = fSample.';
            samplesAdded = samplesAdded + 1;
        else
            failedDirections = failedDirections + 1;
        end
    end

    [updatedIndices, ~] = ...
        radius_local_unique_indices( ...
            sampleX, ...
            xCurrent, ...
            modelRadius, ...
            normalizationRadius, ...
            duplicateDistanceRelative, ...
            maxWindowLength);

    updatedZ = ...
        (sampleX(updatedIndices, :) - ...
         xCurrent.')/normalizationRadius;

    updatedDistances = vecnorm(updatedZ, 2, 2);
    currentTolerance = ...
        100*eps(max(1, norm(xCurrent, 2)))/ ...
        normalizationRadius;

    updatedDisplacements = ...
        updatedZ(updatedDistances > currentTolerance, :);

    [finalGeometryRank, ~] = ...
        normalized_geometry_rank( ...
            updatedDisplacements, ...
            numberOfVariables);

    info.samplesAdded = samplesAdded;
    info.functionEvaluations = functionEvaluations;
    info.failedDirections = failedDirections;
    info.initialGeometryRank = geometryRank;
    info.finalGeometryRank = finalGeometryRank;
    info.modelRadius = modelRadius;
    info.normalizationRadius = normalizationRadius;
    info.budgetExhausted = ...
        isfinite(maximumFunctionEvaluations) && ...
        functionEvaluations >= ...
            maximumFunctionEvaluations;
end


function [xSample, fSample, evaluationCount, success] = ...
        finite_restoration_sample( ...
            fun, xCurrent, direction, requestedStepLength, ...
            sampleX, normalizationRadius, ...
            duplicateDistanceRelative, ...
            maximumEvaluations)
%FINITE_RESTORATION_SAMPLE Try both signs and geometric backoff.

    directionNorm = norm(direction, 2);

    if directionNorm == 0 || ~isfinite(directionNorm)
        xSample = xCurrent;
        fSample = NaN(size(xCurrent));
        evaluationCount = 0;
        success = false;
        return;
    end

    direction = direction/directionNorm;
    stepLength = requestedStepLength;
    evaluationCount = 0;
    success = false;

    xSample = xCurrent;
    fSample = NaN(size(xCurrent));

    for backoffIndex = 1:12

        for signValue = [1, -1]

            candidate = ...
                xCurrent + ...
                signValue*stepLength*direction;

            normalizedDistances = vecnorm( ...
                (sampleX - candidate.')/ ...
                normalizationRadius, ...
                2, ...
                2);

            if any(normalizedDistances <= ...
                    duplicateDistanceRelative)
                continue;
            end

            if evaluationCount >= ...
                    maximumEvaluations
                return;
            end

            candidateResidual = fun(candidate);
            candidateResidual = candidateResidual(:);
            evaluationCount = evaluationCount + 1;

            if all(isfinite(candidate)) && ...
                    all(isfinite(candidateResidual))

                xSample = candidate;
                fSample = candidateResidual;
                success = true;
                return;
            end
        end

        stepLength = 0.5*stepLength;
    end
end


